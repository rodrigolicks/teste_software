package br.com.dagnese.plune.model.types;

public class IntegerPair {
    int value;
    String resolved;

    public IntegerPair() {
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public String getResolved() {
        return resolved;
    }

    public void setResolved(String resolved) {
        this.resolved = resolved;
    }
}
